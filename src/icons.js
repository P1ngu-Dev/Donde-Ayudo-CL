/**
 * Crea un elemento HTML con un icono SVG
 * @param {string} name - Nombre del icono
 * @param {string} className - Clases CSS adicionales
 * @param {string} size - Tama√±o (small, medium, large)
 * @returns {string} HTML del icono
 */
export function createIcon(name, className = '', size = 'medium') {
  const sizes = {
    small: 'w-4 h-4',
    medium: 'w-5 h-5',
    large: 'w-6 h-6',
    xlarge: 'w-8 h-8'
  };
  
  const sizeClass = sizes[size] || sizes.medium;
  const icon = icons[name] || icons.map;
  
  return `<span class="inline-flex ${sizeClass} ${className}">${icon}</span>`;
}

/**
 * Renderiza un icono inline (sin wrapper)
 * @param {string} name - Nombre del icono
 * @returns {string} SVG puro
 */
export function icon(name) {
  return icons[name] || icons.map;
}
